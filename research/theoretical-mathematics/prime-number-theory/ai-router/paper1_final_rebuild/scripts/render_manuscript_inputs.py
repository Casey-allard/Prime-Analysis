def render_lm_tables() -> None:
    ptb = read_csv(TABLES / "ptb_results.csv")
    wt2 = read_csv(TABLES / "wt2_results.csv")

    combined = [
        r"\begin{tabular}{llccc}",
        r"\toprule",
        r"Dataset & Condition & Val.\ PPL & Eff.\ paths & $64/\mathrm{eff}_b$ \\",
        r"\midrule",
    ]
    for dataset, rows in [("PTB", ptb), ("WT2", wt2)]:
        for row in rows:
            combined.append(
                f"{dataset} & {row['condition']} & {fmt(row['mean_val_ppl'])} & "
                f"{fmt(row['mean_eff_buckets'])} & {fmt(row['eff_ratio_vs_dense'])}$\\times$ \\\\"
            )
        if dataset == "PTB":
            combined.append(r"\midrule")
    combined.extend([r"\bottomrule", r"\end{tabular}"])
    write(GENERATED / "table_lm_combined.tex", "\n".join(combined))

    def detail(rows: list[dict[str, str]], include_std: bool, out_name: str) -> None:
        if include_std:
            header = r"\begin{tabular}{lcccc}"
            lines = [header, r"\toprule", r"Condition & Val.\ PPL & Std.\ PPL & Eff.\ paths & $64/\mathrm{eff}_b$ \\", r"\midrule"]
            for row in rows:
                lines.append(
                    f"{row['condition']} & {fmt(row['mean_val_ppl'])} & {fmt(row['std_val_ppl'])} & "
                    f"{fmt(row['mean_eff_buckets'])} & {fmt(row['eff_ratio_vs_dense'])}$\\times$ \\\\"
                )
        else:
            header = r"\begin{tabular}{lcccc}"
            lines = [header, r"\toprule", r"Condition & Val.\ PPL & Eff.\ paths & $64/\mathrm{eff}_b$ & Params \\", r"\midrule"]
            for row in rows:
                params_m = float(row["n_params"]) / 1_000_000.0
                lines.append(
                    f"{row['condition']} & {fmt(row['mean_val_ppl'])} & {fmt(row['mean_eff_buckets'])} & "
                    f"{fmt(row['eff_ratio_vs_dense'])}$\\times$ & {params_m:.2f}M \\\\"
                )
        lines.extend([r"\bottomrule", r"\end{tabular}"])
        write(GENERATED / out_name, "\n".join(lines))

    detail(ptb, include_std=False, out_name="table_ptb_results.tex")
    detail(wt2, include_std=True, out_name="table_wt2_results.tex")


def render_cross_and_null_tables() -> None:
    cross = {row["metric"]: row for row in read_csv(TABLES / "cross_dataset_summary.csv")}
    controls = read_csv(TABLES / "key_control_null_result.csv")

    cross_rows = [
        ("HOPF / BASELINE PPL ratio", cross["hopf_vs_baseline_ppl_ratio"]["PTB"], cross["hopf_vs_baseline_ppl_ratio"]["WT2"], 3, False, True),
        ("HOPF - PERMUTED PPL", cross["hopf_vs_permuted_delta_ppl"]["PTB"], cross["hopf_vs_permuted_delta_ppl"]["WT2"], 2, True, False),
        ("HOPF effective-path ratio", cross["hopf_eff_ratio_vs_dense"]["PTB"], cross["hopf_eff_ratio_vs_dense"]["WT2"], 2, False, True),
        ("BASELINE eff. paths", cross["baseline_mean_eff_buckets"]["PTB"], cross["baseline_mean_eff_buckets"]["WT2"], 1, False, False),
        ("HOPF eff. paths", cross["hopf_mean_eff_buckets"]["PTB"], cross["hopf_mean_eff_buckets"]["WT2"], 1, False, False),
        ("PERMUTED eff. paths", cross["permuted_mean_eff_buckets"]["PTB"], cross["permuted_mean_eff_buckets"]["WT2"], 1, False, False),
    ]
    lines = [r"\begin{tabular}{lcc}", r"\toprule", r"Metric & PTB & WT2 \\", r"\midrule"]
    for label, p, w, digits, signed, mult in cross_rows:
        suffix = r"$\times$" if mult else ""
        lines.append(f"{label} & {fmt(p, digits, signed)}{suffix} & {fmt(w, digits, signed)}{suffix} \\\\")
    lines.extend([r"\bottomrule", r"\end{tabular}"])
    write(GENERATED / "table_cross_dataset_summary.tex", "\n".join(lines))

    null_rows = [row for row in controls if "null" in row["control_case"].lower()]
    lines = [r"\begin{tabular}{lcccc}", r"\toprule", r"Dataset & HOPF / BASE & HOPF - PERM & HOPF eff. paths & PERM eff. paths \\", r"\midrule"]
    for row in null_rows:
        lines.append(
            f"{row['dataset_or_setting']} & {fmt(row['hopf_vs_baseline_ratio'], 3)} & "
            f"{fmt(row['hopf_minus_permuted_ppl'], 2, True)} & {fmt(row['hopf_mean_eff_b'], 1)} & {fmt(row['permuted_mean_eff_b'], 1)} \\\\"
        )
    lines.extend([r"\bottomrule", r"\end{tabular}"])
    write(GENERATED / "table_key_null_main.tex", "\n".join(lines))

    lines = [
        r"\begin{tabular}{p{0.19\linewidth}p{0.10\linewidth}p{0.10\linewidth}p{0.10\linewidth}p{0.36\linewidth}}",
        r"\toprule",
        r"Case & HOPF / BASE & HOPF - PERM & Aux eff. paths & Interpretation \\",
        r"\midrule",
    ]
    for row in controls:
        aux_text = "--" if not row["learned_sparse_mean_eff_b"] else fmt(row["learned_sparse_mean_eff_b"], 1)
        lines.append(
            f"{latex_escape(row['dataset_or_setting'])} & {fmt(row['hopf_vs_baseline_ratio'], 3)} & "
            f"{fmt(row['hopf_minus_permuted_ppl'], 2, True)} & {aux_text} & "
            f"{latex_escape(row['interpretation'])} \\\\"
        )
    lines.extend([r"\bottomrule", r"\end{tabular}"])
    write(GENERATED / "table_key_control_null_result.tex", "\n".join(lines))


