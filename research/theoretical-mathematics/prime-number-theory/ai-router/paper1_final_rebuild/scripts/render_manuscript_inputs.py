def render_proxy_control_figure() -> None:
    inc0143 = route_stats_by_id(read_json(ANALYSIS / "inc0143_ppmi_semantic_finalize.json"))
    inc0144 = route_stats_by_id(read_json(ANALYSIS / "inc0144_hopf_vs_kmeans_stage3_confirm.json"))

    fig, axes = plt.subplots(1, 2, figsize=(6.6, 2.7))

    labels_a = ["Structured", "Col-perm"]
    pmax_a = [inc0143["SEM_ORIG"]["mean_sector_pmax"], inc0143["SEM_COL_PERM"]["mean_sector_pmax"]]
    entropy_a = [inc0143["SEM_ORIG"]["mean_sector_entropy"], inc0143["SEM_COL_PERM"]["mean_sector_entropy"]]

    ax = axes[0]
    x = range(len(labels_a))
    ax.bar(x, pmax_a, color=["#1d4ed8", "#9ca3af"], width=0.55)
    ax.set_xticks(list(x), labels_a)
    ax.set_ylabel(r"Sector $p_{\max}$")
    ax.set_title("Structured signal vs column permutation", fontsize=9)
    ax.set_ylim(0.0, max(pmax_a) * 1.35)
    for i, val in enumerate(pmax_a):
        ax.text(i, val + 0.002, f"{val:.3f}", ha="center", va="bottom", fontsize=8)
    ax2 = ax.twinx()
    ax2.plot(list(x), entropy_a, color="#b45309", marker="o", linewidth=1.4)
    ax2.set_ylabel("Entropy", color="#b45309")
    ax2.tick_params(axis="y", colors="#b45309", labelsize=7)

    labels_b = ["Hopf", "$K$-means", "Hopf perm", "$K$-means perm"]
    pmax_b = [
        inc0144["HOPF_ORIG"]["mean_sector_pmax"],
        inc0144["KMEANS_ORIG"]["mean_sector_pmax"],
        inc0144["HOPF_PERM"]["mean_sector_pmax"],
        inc0144["KMEANS_PERM"]["mean_sector_pmax"],
    ]
    entropy_b = [
        inc0144["HOPF_ORIG"]["mean_sector_entropy"],
        inc0144["KMEANS_ORIG"]["mean_sector_entropy"],
        inc0144["HOPF_PERM"]["mean_sector_entropy"],
        inc0144["KMEANS_PERM"]["mean_sector_entropy"],
    ]

    ax = axes[1]
    x = range(len(labels_b))
    ax.bar(x, pmax_b, color=["#1d4ed8", "#0f766e", "#93c5fd", "#99f6e4"], width=0.58)
    ax.set_xticks(list(x), labels_b, rotation=20, ha="right")
    ax.set_ylabel(r"Sector $p_{\max}$")
    ax.set_title("Hopf vs adaptive $K$-means", fontsize=9)
    ax.set_ylim(0.0, max(pmax_b) * 1.35)
    for i, val in enumerate(pmax_b):
        ax.text(i, val + 0.0015, f"{val:.3f}", ha="center", va="bottom", fontsize=7.5)
    ax2 = ax.twinx()
    ax2.plot(list(x), entropy_b, color="#b45309", marker="o", linewidth=1.4)
    ax2.set_ylabel("Entropy", color="#b45309")
    ax2.tick_params(axis="y", colors="#b45309", labelsize=7)

    fig.suptitle("Proxy controls show concentration follows structured geometry", fontsize=10, y=1.04)
    fig.tight_layout()
    fig.savefig(FIGURES / "paper1_proxy_controls.pdf", bbox_inches="tight")
    fig.savefig(FIGURES / "paper1_proxy_controls.png", bbox_inches="tight", dpi=180)
    plt.close(fig)


