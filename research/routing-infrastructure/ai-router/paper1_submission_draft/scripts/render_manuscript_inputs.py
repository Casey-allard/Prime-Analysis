def render_cross_dataset_table() -> None:
    cross = {row["metric"]: row for row in read_csv("cross_dataset_summary.csv")}
    rows = [
        ("HOPF / BASELINE PPL ratio", cross["hopf_vs_baseline_ppl_ratio"]["PTB"], cross["hopf_vs_baseline_ppl_ratio"]["WT2"], 3, False),
        ("HOPF - PERMUTED PPL", cross["hopf_vs_permuted_delta_ppl"]["PTB"], cross["hopf_vs_permuted_delta_ppl"]["WT2"], 2, True),
        ("HOPF effective-path ratio", cross["hopf_eff_ratio_vs_dense"]["PTB"], cross["hopf_eff_ratio_vs_dense"]["WT2"], 2, False),
        ("BASELINE eff. paths", cross["baseline_mean_eff_buckets"]["PTB"], cross["baseline_mean_eff_buckets"]["WT2"], 1, False),
        ("HOPF eff. paths", cross["hopf_mean_eff_buckets"]["PTB"], cross["hopf_mean_eff_buckets"]["WT2"], 1, False),
        ("PERMUTED eff. paths", cross["permuted_mean_eff_buckets"]["PTB"], cross["permuted_mean_eff_buckets"]["WT2"], 1, False),
    ]

    lines = [
        r"\begin{tabular}{lcc}",
        r"\toprule",
        r"Metric & PTB & WT2 \\",
        r"\midrule",
    ]
    for label, ptb_value, wt2_value, digits, signed in rows:
        suffix = r"$\times$" if "ratio" in label.lower() and "PPL" not in label else ""
        lines.append(
            f"{label} & {fmt(ptb_value, digits, signed)}{suffix} & {fmt(wt2_value, digits, signed)}{suffix} \\\\"
        )
    lines.extend([r"\bottomrule", r"\end{tabular}"])
    write(GENERATED / "table_cross_dataset_summary.tex", "\n".join(lines))


def render_key_null_table() -> None:
    controls = read_csv("key_control_null_result.csv")
    null_rows = [row for row in controls if "null" in row["control_case"].lower()]

    lines = [
        r"\begin{tabular}{lcccc}",
        r"\toprule",
        r"Dataset & HOPF / BASE & HOPF - PERM & HOPF eff. paths & PERM eff. paths \\",
        r"\midrule",
    ]
    for row in null_rows:
        lines.append(
            f"{row['dataset_or_setting']} & {fmt(row['hopf_vs_baseline_ratio'], 3)} & "
            f"{fmt(row['hopf_minus_permuted_ppl'], 2, True)} & "
            f"{fmt(row['hopf_mean_eff_b'], 1)} & {fmt(row['permuted_mean_eff_b'], 1)} \\\\"
        )
    lines.extend([r"\bottomrule", r"\end{tabular}"])
    write(GENERATED / "table_key_null_main.tex", "\n".join(lines))


def render_key_control_appendix_table() -> None:
    controls = read_csv("key_control_null_result.csv")
    lines = [
        r"\begin{tabular}{p{0.22\linewidth}p{0.11\linewidth}p{0.11\linewidth}p{0.11\linewidth}p{0.33\linewidth}}",
        r"\toprule",
        r"Case & HOPF / BASE & HOPF - PERM & Aux eff. paths & Interpretation \\",
        r"\midrule",
    ]
    for row in controls:
        case = row["dataset_or_setting"].replace("_", r"\_")
        aux_eff = row["learned_sparse_mean_eff_b"]
        aux_text = fmt(aux_eff, 1) if aux_eff else "--"
        lines.append(
            f"{case} & {fmt(row['hopf_vs_baseline_ratio'], 3)} & "
            f"{fmt(row['hopf_minus_permuted_ppl'], 2, True)} & {aux_text} & "
            f"{tex_escape(row['interpretation'])} \\\\"
        )
    lines.extend([r"\bottomrule", r"\end{tabular}"])
    write(GENERATED / "table_key_control_null_result.tex", "\n".join(lines))


def main() -> None:
    GENERATED.mkdir(parents=True, exist_ok=True)
    render_metrics()
    render_setup_table()
    render_combined_lm_table()
    render_ptb_table()
    render_wt2_table()
    render_cross_dataset_table()
    render_key_null_table()
    render_key_control_appendix_table()
    print(f"Rendered manuscript inputs in {GENERATED.relative_to(ROOT)}")


if __name__ == "__main__":
    main()
