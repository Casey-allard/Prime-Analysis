#!/usr/bin/env python3
"""Render manuscript-facing LaTeX inputs from canonical Paper 1 assets.

This script reads the generated CSV/JSON artifacts under ``paper1_assets/`` and
writes LaTeX snippets for the submission draft. The goal is to keep manuscript
tables and key numeric claims synchronized with the canonical analysis exports
rather than retyping them into LaTeX.
"""

from __future__ import annotations

import csv
import json
from pathlib import Path


ROOT = Path(__file__).resolve().parents[2]
ASSETS = ROOT / "paper1_assets"
TABLES = ASSETS / "tables"
DATA = ASSETS / "data"
DRAFT = ROOT / "paper1_submission_draft"
GENERATED = DRAFT / "generated"


def read_csv(name: str) -> list[dict[str, str]]:
    with (TABLES / name).open("r", encoding="utf-8", newline="") as handle:
        return list(csv.DictReader(handle))


def read_json(name: str) -> dict:
    with (DATA / name).open("r", encoding="utf-8") as handle:
        return json.load(handle)


def fmt(value: str, digits: int = 2, signed: bool = False) -> str:
    number = float(value)
    if signed:
        return f"{number:+.{digits}f}"
    return f"{number:.{digits}f}"


def tex_escape(text: str) -> str:
    replacements = {
        "\\": r"\textbackslash{}",
        "&": r"\&",
        "%": r"\%",
        "$": r"\$",
        "#": r"\#",
        "_": r"\_",
    }
    for source, target in replacements.items():
        text = text.replace(source, target)
    return text


def write(path: Path, content: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content.strip() + "\n", encoding="utf-8")


def render_metrics() -> None:
    ptb = read_csv("ptb_results.csv")
    wt2 = read_csv("wt2_results.csv")
    cross = {row["metric"]: row for row in read_csv("cross_dataset_summary.csv")}
    controls = read_csv("key_control_null_result.csv")
    fit = read_json("scaling_fit_params.json")

    ptb_map = {row["condition"]: row for row in ptb}
    wt2_map = {row["condition"]: row for row in wt2}
    aux_row = next(row for row in controls if row["control_case"] == "Baseline_rationale_aux_loss_control")
    inc0170 = json.loads((ROOT / "router-research" / "results" / "analysis" / "inc0170_large_k.json").read_text())
    ratio_400 = inc0170["compression_by_K"]["400"]["eff_ratio_PERM_over_ORIG"]
    ratio_5000 = inc0170["compression_by_K"]["5000"]["eff_ratio_PERM_over_ORIG"]

    lines = [
        "% Auto-generated by scripts/render_manuscript_inputs.py",
        rf"\newcommand{{\PaperHopfScalingA}}{{{fit['anchor_fit_reference']['TRANS_ORIG']['c']:.3f}}}",
        rf"\newcommand{{\PaperHopfScalingAlpha}}{{{fit['anchor_fit_reference']['TRANS_ORIG']['alpha']:.3f}}}",
        rf"\newcommand{{\PaperPermScalingAlpha}}{{{fit['anchor_fit_reference']['TRANS_PERM']['alpha']:.3f}}}",
        rf"\newcommand{{\PaperHopfLargeKAlpha}}{{{fit['large_k_fit_reference']['TRANS_ORIG']['alpha']:.3f}}}",
        rf"\newcommand{{\PaperRatioAtFourHundred}}{{{ratio_400:.2f}}}",
        rf"\newcommand{{\PaperRatioAtFiveThousand}}{{{ratio_5000:.2f}}}",
        rf"\newcommand{{\PaperPTBHopfBaselineRatio}}{{{fmt(cross['hopf_vs_baseline_ppl_ratio']['PTB'], 3)}}}",
        rf"\newcommand{{\PaperWTTwoHopfBaselineRatio}}{{{fmt(cross['hopf_vs_baseline_ppl_ratio']['WT2'], 3)}}}",
        rf"\newcommand{{\PaperPTBHopfPermDelta}}{{{fmt(cross['hopf_vs_permuted_delta_ppl']['PTB'], 2, signed=True)}}}",
        rf"\newcommand{{\PaperWTTwoHopfPermDelta}}{{{fmt(cross['hopf_vs_permuted_delta_ppl']['WT2'], 2, signed=True)}}}",
        rf"\newcommand{{\PaperPTBHopfEff}}{{{fmt(ptb_map['HOPF']['mean_eff_buckets'], 1)}}}",
        rf"\newcommand{{\PaperWTTwoHopfEff}}{{{fmt(wt2_map['HOPF']['mean_eff_buckets'], 1)}}}",
        rf"\newcommand{{\PaperAuxSparseEff}}{{{fmt(aux_row['learned_sparse_mean_eff_b'], 1)}}}",
    ]
    write(GENERATED / "metrics.tex", "\n".join(lines))


